/**
 * 根据错误码获取错误信息
 * @param errCode 错误码
 * @param defaultMsg 默认错误信息（当错误码未匹配时使用）
 * @returns 格式化的错误信息
 */
export function getErrorMessage(errCode: number, defaultMsg?: string): string {
	const message = QingflowErrorMessages[errCode];
	if (message) {
		return `[${errCode}] ${message}`;
	}
	// 如果没有匹配到错误码，返回错误码和默认信息
	return defaultMsg ? `[${errCode}] ${defaultMsg}` : `[${errCode}] 未知错误`;
}
